#ifndef _FADE_H_
#define _FADE_H_
/*******************************************************************************
* インクルードファイル
*******************************************************************************/
#include "main.h"

/*******************************************************************************
* マクロ定義
*******************************************************************************/

//フェードの状態
typedef enum
{
	FADE_NONE = 0,	//非動作時
	FADE_IN,		//フェードイン状態
	FADE_OUT,		//フェードアウト状態
	FADE_OUT_WHITE,		//フェードアウト状態（白）
	FADE_MAX		//フェードのモード数
}FADE;

/*******************************************************************************
* 構造体定義
*******************************************************************************/

/*******************************************************************************
* プロトタイプ宣言
*******************************************************************************/

/*******************************************************************************
* 関数名	:HRESULT InitFade( void )
*		フェード初期化関数です。
* 引数		:void 引数なし
* 戻り値	:HRESULT 関数の実行結果
* 説明		:変数等の初期化を実行する起動毎に一度のみ動作する関数です。
*******************************************************************************/
HRESULT InitFade( void );

/*******************************************************************************
* 関数名	:void UninitFade( void )
*		フェード終了処理関数です。
* 引数		:void 引数なし
* 戻り値	:void 戻り値なし
* 説明		:終了時に実行する関数を内包する関数で終了時に一度のみ動作。
*******************************************************************************/
void UninitFade( void );

/*******************************************************************************
* 関数名	:void UpdateFade( void )
*		フェード更新処理関数です。
* 引数		:void 引数なし
* 戻り値	:void 戻り値なし
* 説明		:処理周回毎の処理内容を記述します。実質的なメイン関数です。
*******************************************************************************/
void UpdateFade( void );

/*******************************************************************************
* 関数名	:void DrawFade( void )
*		フェード描画処理関数です。
* 引数		:void 引数なし
* 戻り値	:void 戻り値なし
* 説明		:画面出力に関わる処理を内包します。
*******************************************************************************/
void DrawFade( void );


/*******************************************************************************
* 関数名	:void SetFade( FADE fade , MODE modeNext )
*		フェードの状態を設定します。
* 引数		:FADE fade		フェードの設定です。
*			 MODE modeNext	フェードアウト時の切り替え先です。
* 戻り値	:void 戻り値なし
* 説明		:フェードの状態を設定します。
*******************************************************************************/
void SetFade( FADE fade , MODE modeNext );

/*******************************************************************************
* 関数名	:FADE GetFade( void )
*		フェードの状態を取得します。
* 引数		:void 引数なし
* 戻り値	:FADE	現在のフェード状態
* 説明		:フェードの状態を取得します。
*******************************************************************************/
FADE GetFade( void );

#endif